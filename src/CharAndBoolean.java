public class CharAndBoolean {
    public static void main(String[] args) {

        /*
            Char veri tipi 16 bit uzunluğundadır. Karakterleri tanımlamak için kullanılır. Java UNICODE karakter seti
            kullanır ve tüm dilleri çevirir.
         */

        char karakter = 117; // 117 ASCII kod'da neye karşılık gelirse ekranda onu gösterecek.
        System.out.println("117'nin Char değeri = "+karakter);

        /*
            Boolean veri tipi True yada False döndürür. Genellikle koşul ve döngü işlemlerinde kontrol amaçlı olarak
            kullanılır.
         */

        boolean mantik1 = true;
        boolean mantik2 = false;
        System.out.println("Boolean Değeri = " + mantik1 + " ve " + mantik2);


    }
}
