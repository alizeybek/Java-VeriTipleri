public class OndalikVeriTipleri {
    public static void main(String[] args) {

        /*
            Float veri tipi 32 bit uzunluğundadır. 1.4x10^-45 ile 3.4x10^38 aralığında değerleri alır. Float ile
            double'ı birbirinden ayırmak için tanımlanan sayının sonuna f veya F konulmalıdır.
         */

        float sayi1=34.0f;
        float sayi2=35.6f;
        float sayi3=36;
        System.out.println("Float Değeri = "+sayi1
        + " ve " + sayi2 + " ve " + sayi3);

        /*
            Double veri tipi 64 bit uzunluğundadır. 4.9x10^-324 ile 1.8x10^308 aralığında değerleri alır.
         */

        double ondalikliSayi1=3;
        double ondalikliSayi2=3.14;
        System.out.println("Double Değeri = "+ondalikliSayi1
        + " ve " + ondalikliSayi2);







    }
}
