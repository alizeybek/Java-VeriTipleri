public class SayisalVeriTipleri {

    public static void main(String[] args) {

        /*
            Byte veri tipi 8 Bit uzunluğundadır. 127 ile -128 arasında değer alır.
         */

        byte abc = 100;
        System.out.println("Byte Değeri = "+abc);

        /*
            Short veri tipi 16 Bit uzunluğundadır. 32.767 ile -32.768 arasında değer alır.
         */

        short shortDegiskeni=1000;
        System.out.println("Short Değeri = "+shortDegiskeni);

       /*
            Integer veri tipi 32 Bit uzunluğundadır. 2.147.483.647 ile -2.147.483.648 arasında değer alır.
        */

        int intDeger = 100000;
        System.out.println("İnt Değeri = "+intDeger);

        /*
            Long veri tipi 64 Bit uzunluğundadır. 9.223.372.036.854.775.807 ile -9.223.372.036.854.775.808
            arasında değer alır.
         */

        long longDeger = 100000000;
        System.out.println("Long Değeri = "+longDeger);





    }

}
